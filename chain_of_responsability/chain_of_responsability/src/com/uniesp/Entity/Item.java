package com.uniesp.Entity;

public class Item {

    private String nome;
    private Double valor;

    public Item() {}

    public String getNome() {
        return this.nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Double getValor() {
        return this.valor;
    }

    public void setValor(Double valor) {
        this.valor= valor;
    }
}
